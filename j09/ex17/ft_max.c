int		ft_max(int *tab, int lenght)
{
	int i;
	int k;

	i = -1;
	k = 0;
	while (i++ <= lenght)
	{
		while (tab[i] >= tab[k])
		{
			if (k == lenght)
				return (tab[i]);
			k++;
		}
		k = 0;
	}
	return (0);
}
