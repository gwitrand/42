int		ft_bouton(int i, int j, int k)
{
	int swap;

	swap = 0;
	if (i >= j)
	{
		swap = i;
		i = j;
		j = swap;
	}
	if (j >= k)
	{
		swap = j;
		j = k;
		k = swap;
	}
	if (i >= j)
	{
		swap = i;
		i = j;
		j = swap;
	}
	return (j);
}
