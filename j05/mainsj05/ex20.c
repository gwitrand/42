int     ft_putchar(char c)
{
    write(1, &c, 1);
    return (0);
}

void            decomp(int nb)
{
    if (nb > 1)
        decomp(nb >> 1);
    ft_putchar('0' + (nb & 1));
}

int     main(void)
{
	int nb = 126;
	decomp(nb);
    return (0);
}

